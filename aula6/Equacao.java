public class Equacao {

    private double a;
    private double b;
    private double c;
    private double delta;

    //#region Getters e Setter
    public void setA(double a) {
        this.a = a;
    }
    public double getA() {
        return a;
    }
    public void setB(double b) {
        this.b = b;
    }
    public double getB() {
        return b;
    }
    public void setC(double c) {
        this.c = c;
    }
    public double getC() {
        return c;
    }
    public void setDelta(double delta) {
        this.delta = delta;
    }
    public double getDelta() {
        return delta;
    }

    //#endregion
}